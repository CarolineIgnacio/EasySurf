    private void jfHorasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jfHorasActionPerformed
        int nrHoras = Integer.getInteger(jfHoras.getText());
        if ( nrHoras < 0) {
           JOptionPane.showMessageDialog(null, "O nÃºmero de horas DEVE ser positivo");
        }
        else{
            if (jfValor.getValue() != null){
                jfTotal.setValue(nrHoras * Float.valueOf(jfValor.getText()));
            }
        }
    }//GEN-LAST:event_jfHorasActionPerformed

    private void jfValorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jfValorActionPerformed
        float valor = Float.valueOf(jfValor.getText());
        int nrHoras = Integer.getInteger(jfHoras.getText());
        if ( valor < 0) {
           JOptionPane.showMessageDialog(null, "O valor por hora DEVE ser positivo");
        }
        else{
            if (jfHoras.getValue() != null){
                jfTotal.setValue(nrHoras * valor);
            }
        }
    }//GEN-LAST:event_jfValorActionPerformed

